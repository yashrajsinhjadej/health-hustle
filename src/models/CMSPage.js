// models/CMSPage.js
const mongoose = require('mongoose');

const cmsPageSchema = new mongoose.Schema({
  slug: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true
  },
  title: {
    type: String,
    required: true,
    trim: true
  },
  htmlContent: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

// Index for faster queries
cmsPageSchema.index({ slug: 1 });

module.exports = mongoose.model('CMSPage', cmsPageSchema);